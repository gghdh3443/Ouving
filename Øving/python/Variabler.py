# Oppgave 1 - grunneleggende variabler

x = 10 # X har verdien 10.
y = "Johan" # Y har verdien "Johan".
print(x) # Skriver ut verdien av x
print(y) # Skriver ut verdien av y.

# Oppgave 2 - endre variabler

x = 5 # Endrer verdien av x til 5.
y = "Ola" # Endrer verdien av y til "Ola".
print(x) # Skriver ut den nye verdien av x.
print(y) # Skriver ut den nye verdien av y.

# Oppgave 3 - bruke variabler i beregninger

a = 10
b = 5
c = 8

sum = a + b + c # Beregner summen av a, b og c.
print("Summen av a, b og c er:", sum) # Skriver ut summen av a, b og c.

